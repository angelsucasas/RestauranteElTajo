    private void listadoEmpleadosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listadoEmpleadosMouseClicked
        // TODO add your handling code here:
        if(listadoEmpleados.getSelectedRow()!=-1){
            cedulaEmpleado.setText(cedula_empleados.get(listadoEmpleados.getSelectedRow()));
            NombresEmpleado.setText(solo_nombres_empleados.get(listadoEmpleados.getSelectedRow()));
            ApellidosEmpleado.setText(solo_apellidos_empleados.get(listadoEmpleados.getSelectedRow()));
            CargosEmpleado.setSelectedItem(cargo_empleados.get(listadoEmpleados.getSelectedRow()));
        } 
        
    }//GEN-LAST:event_listadoEmpleadosMouseClicked

    private void ActualizarEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ActualizarEmpleadoActionPerformed
        // TODO add your handling code here:
        if(listadoEmpleados.getRowCount()!= 0 && listadoEmpleados.getSelectedRow()!= -1){
            try{
                Empleado empleado_seleccionada_por_usuario = (Empleado)micontrol.filtrarEmpleadoPorCedula(cedula_empleados.get(listadoEmpleados.getSelectedRow()));                
                micontrol.modificarEmpleado(cedulaEmpleado.getText(),NombresEmpleado.getText() , ApellidosEmpleado.getText(), CargosEmpleado.getSelectedItem().toString(),  cedula_empleados.get(listadoEmpleados.getSelectedRow()));                
                cargarEmpleados();
            }catch(Exception e){
                //este catch es necesario ya que ocurre un error fuera del index si el usuario selecciona un elemento a modificar y luego realiza la creacion/eliminacion de algun elemento, antes de modificarlo
                //esto causa que el numero de elementos disminuya y pueda ocurrir un error fuera de indice. ej: si queriamos modificar el elemento 5, pero tras eliminar un elemento ahora hay 4, entonces al tratar de acceder al 5 todo explota
                JOptionPane.showMessageDialog(rootPane, "error al modificar el empleado, por favor repita los pasos e intente de nuevo, error:"+ e.getMessage());
            }
            
        }else{
            if(listadoEmpleados.getRowCount()== 0){ JOptionPane.showMessageDialog(rootPane, "debe seleccionar el plato que desea modificar ");}        
        }
    }//GEN-LAST:event_ActualizarEmpleadoActionPerformed

