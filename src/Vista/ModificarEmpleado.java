    public void cargarEmpleados(){
        nombre_completo_empleados = micontrol.getNombreCompletoPorEmpleados();
        solo_nombres_empleados = micontrol.getNombrePorEmpleados();
        solo_apellidos_empleados = micontrol.getApellidoPorEmpleados();
        cedula_empleados = micontrol.getCedulasPorEmpleado();
        cargo_empleados = micontrol.getCargosPorEmpleados();
        
        DefaultTableModel modelT = (DefaultTableModel) listadoEmpleados.getModel();                                                  
        
        codigos = new String[cedula_empleados.size()];
        
        modelT.setRowCount(0);
        for (int i = 0; i < codigos.length; i++) {
            codigos[i] = cedula_empleados.get(i);
            modelT.addRow(new Object[]{cedula_empleados.get(i), nombre_completo_empleados.get(i),  cargo_empleados.get(i)});
        }
    }
    
    public void cargarCargos(){
       cargos = micontrol.getCargos();
       CargosEmpleado.setModel(new DefaultComboBoxModel<>(cargos));       
    }

