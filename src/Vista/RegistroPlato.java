    private void guardarPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarPlatoActionPerformed
       // TODO add your handling code here:
       if(listaCategoria.getItemCount() != 0 && !nombrePlato.getText().isEmpty() && !nombrePlato.getText().isBlank() && !precioPlato.getText().isEmpty() && !precioPlato.getText().isBlank()){
            Categoria categoria_seleccionada_por_usuario = (Categoria)micontrol.filtrarCategoriaPorCodigo(micontrol.getCategorias(), codigos_categoria[listaCategoria.getSelectedIndex()]);
            micontrol.crearComida(nombrePlato.getText(),descripcionPlato.getText() , precioPlato.getText(), categoria_seleccionada_por_usuario );
            cargarComida();
       }else{
           if(listaCategoria.getItemCount() == 0) JOptionPane.showMessageDialog(rootPane, "debe registrar alguna categoria primero");
           if(nombrePlato.getText().isBlank()) JOptionPane.showMessageDialog(rootPane, "debe ingresar un nombre para el plato");
           if(precioPlato.getText().isBlank()) JOptionPane.showMessageDialog(rootPane, "debe ingresar un precio para el plato");
       }
    }//GEN-LAST:event_guardarPlatoActionPerformed

    private void modificarPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modificarPlatoActionPerformed
        // TODO add your handling code here:        
        if(listaCategoria.getItemCount() != 0 && listadoPlatos.getRowCount() != 0){
            try{
                Categoria categoria_seleccionada_por_usuario = (Categoria)micontrol.filtrarCategoriaPorCodigo(micontrol.getCategorias(), codigos_categoria[listaCategoria.getSelectedIndex()]);
                micontrol.modificarComida(nombrePlato.getText(),descripcionPlato.getText() , precioPlato.getText(), categoria_seleccionada_por_usuario, codigos[listadoPlatos.getSelectedRow()] );
                cargarComida();
            }catch(Exception e){
                //este catch es necesario ya que ocurre un error fuera del index si el usuario selecciona un elemento a modificar y luego realiza la creacion/eliminacion de algun elemento, antes de modificarlo
                //esto causa que el numero de elementos disminuya y pueda ocurrir un error fuera de indice. ej: si queriamos modificar el elemento 5, pero tras eliminar un elemento ahora hay 4, entonces al tratar de acceder al 5 todo explota
                JOptionPane.showMessageDialog(rootPane, "error al modificar el plato, por favor repita los pasos e intente de nuevo, error:"+ e.getMessage());
            }
            
        }else{
            if(listadoPlatos.getRowCount()== 0){ JOptionPane.showMessageDialog(rootPane, "debe seleccionar el plato que desea modificar ");}
            if(listaCategoria.getItemCount()== 0){ JOptionPane.showMessageDialog(rootPane, "debe registrar alguna categoria primero ");}            
        }
    }//GEN-LAST:event_modificarPlatoActionPerformed

    private void eliminarPlatoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarPlatoActionPerformed
        // TODO add your handling code here:
        if(listadoPlatos.getRowCount() != 0 &&  listadoPlatos.getSelectedRow() != -1){
            micontrol.eliminarComida(codigos[listadoPlatos.getSelectedRow()]);
            cargarComida();            
        }else{
            JOptionPane.showMessageDialog(rootPane, "debe seleccionar la plato a eliminar");
        }
    }//GEN-LAST:event_eliminarPlatoActionPerformed

