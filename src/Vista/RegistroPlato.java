    public void cargarComida(){
        nombre_comidas = micontrol.filtrarComidasPorNombre();
        codigo_comidas = micontrol.getComidasPorCodigo();        
        precio_comidas = micontrol.filtrarComidasPorPrecio();
        descripcion_comidas = micontrol.filtrarComidasPorDescripcion();
        categoria_comidas = micontrol.filtrarComidasPorCategoria();
        
        DefaultTableModel modelT = (DefaultTableModel) listadoPlatos.getModel();                                                  
        
        codigos = new int[codigo_comidas.size()];
        
        modelT.setRowCount(0);
        for (int i = 0; i < codigos.length; i++) {
            codigos[i] = codigo_comidas.get(i);
            modelT.addRow(new Object[]{nombre_comidas.get(i), descripcion_comidas.get(i), precio_comidas.get(i),  categoria_comidas.get(i)});
        }
    }
    
    public void cargarCategorias(){
        List<String> nombre_categorias = micontrol.filtrarCategoriasPorNombre();
        List<Integer> codigo_categorias = micontrol.getCategoriasPorCodigo();
        
        codigos_categoria = new int[codigo_categorias.size()];
        for (int i = 0; i < codigos_categoria.length; i++) {
            codigos_categoria[i] = codigo_categorias.get(i);
        }
        nombres = nombre_categorias.toArray(nombres);
        
        listaCategoria.removeAllItems();
        listaCategoria.setModel(new DefaultComboBoxModel<>(nombre_categorias.toArray(nombres)));
    }

