     public void cargarMenu(){
        nombre_menus = micontrol.filtrarMenuPorNombre();
        codigo_menus = micontrol.getMenuPorCodigo();
        
        nombre_combos = micontrol.filtrarCombosPorNombre();
        codigo_combos = micontrol.getCombosPorCodigo();  

        nombre_comidas = micontrol.filtrarComidasPorNombre();        
        codigo_comidas = micontrol.getComidasPorCodigo();
                
        comidas_por_cada_menu = micontrol.getComidasDelMenu();
        combos_por_cada_menu = micontrol.getStringCombosDelMenu(); 
        
        DefaultTableModel modelT = (DefaultTableModel) listadoDeCombos.getModel();                                                  
        
        codigos = new int[nombre_menus.size()];
        
        modelT.setRowCount(0);
        for (int iteradorCombos = 0; iteradorCombos < codigos.length; iteradorCombos++) {
            codigos[iteradorCombos] = codigo_menus.get(iteradorCombos);
            modelT.addRow(new Object[]{nombre_menus.get(iteradorCombos), combos_por_cada_menu.get(iteradorCombos) , comidas_por_cada_menu.get(iteradorCombos)});
        }
               
        listaCombosDisponibles.removeAllItems();
        listaCombosDisponibles.setModel(new DefaultComboBoxModel<>(nombre_combos.toArray(nombres)));
        
        listaPlatoDisponibles.removeAllItems();
        listaPlatoDisponibles.setModel(new DefaultComboBoxModel<>(nombre_comidas.toArray(nombres)));
    }
     
