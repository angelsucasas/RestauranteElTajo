    private void guardarComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarComboActionPerformed
        // TODO add your handling code here:
        if((listaCombosAgregados.getItemCount() != 0 || listaPlatoAgregados.getItemCount() != 0) && !nombreMenu.getText().isBlank() && !nombreMenu.getText().isEmpty()){

            List<Comida> comidas = new ArrayList<>();
            List<Combo> combos = new ArrayList<>();

            if(listaCombosAgregados.getItemCount() != 0){
                for (int indexCombosAAgregar = 0; indexCombosAAgregar < listaCombosAgregados.getItemCount() ; indexCombosAAgregar++) {
                    int  codigo_combo_a_registrar = codigo_combos.get(nombre_combos.indexOf(listaCombosAgregados.getItemAt(indexCombosAAgregar)));
                    combos.add((Combo)micontrol.filtrarComboPorCodigo(micontrol.getCombos(), codigo_combo_a_registrar));
                }                
            }
            
            if(listaPlatoAgregados.getItemCount() != 0){
                for (int indexPlatosAAgregar = 0; indexPlatosAAgregar < listaPlatoAgregados.getItemCount() ; indexPlatosAAgregar++) {
                    int  codigo_comida_a_registrar = codigo_comidas.get(nombre_comidas.indexOf(listaPlatoAgregados.getItemAt(indexPlatosAAgregar)));
                    comidas.add((Comida)micontrol.filtrarComidaPorCodigo( codigo_comida_a_registrar));
                }
            }
            
            micontrol.crearMenu(nombreMenu.getText(), comidas, combos);
            cargarMenu();
        }else{
            if(listaPlatoAgregados.getItemCount()== 0 && listaCombosAgregados.getItemCount()== 0){ JOptionPane.showMessageDialog(rootPane, "debe registrar algun plato y/o combo primero ");}            
            if(nombreMenu.getText().isEmpty()) JOptionPane.showMessageDialog(rootPane, "debe registrar algun nombre para el menu");
        }
    }//GEN-LAST:event_guardarComboActionPerformed

