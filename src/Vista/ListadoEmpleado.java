    private void eliminarEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eliminarEmpleadosActionPerformed
        // TODO add your handling code here:
        if(listadoEmpleados.getRowCount() != 0 &&  listadoEmpleados.getSelectedRow() != -1){
            micontrol.eliminarEmpleado(codigos[listadoEmpleados.getSelectedRow()]);
            cargarEmpleados();            
        }else{
            JOptionPane.showMessageDialog(rootPane, "debe seleccionar el empleado a eliminar");
        }
    }//GEN-LAST:event_eliminarEmpleadosActionPerformed

