    public void cargarEmpleados(){
        List<String> nombre_completo_empleados = micontrol.getNombreCompletoPorEmpleados();
        List<String> cedula_empleados = micontrol.getCedulasPorEmpleado();
        List<String> cargo_empleados = micontrol.getCargosPorEmpleados();
        
        DefaultTableModel modelT = (DefaultTableModel) listadoEmpleados.getModel();                                                  
        
        codigos = new String[cedula_empleados.size()];
        
        modelT.setRowCount(0);
        for (int i = 0; i < codigos.length; i++) {
            codigos[i] = cedula_empleados.get(i);
            modelT.addRow(new Object[]{cedula_empleados.get(i), nombre_completo_empleados.get(i),  cargo_empleados.get(i)});
        }
    }

