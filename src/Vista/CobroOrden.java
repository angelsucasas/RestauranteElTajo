    private void listadoDeOrdenesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_listadoDeOrdenesMouseClicked
        // TODO add your handling code here:
        if(listadoDeOrdenes.getSelectedRow() != -1){
            Pedido pedido_seleccionado = (Pedido)micontrol.filtrarPedidoPorCodigo(
                    this.pedidos,
                    codigos.get(listadoDeOrdenes.getSelectedRow())
            );    
            
            double iva = pedido_seleccionado.getPrecioTotal()*0.16;
            CampoIVAOrden.setText( iva+"");
            PrecioFinal.setText((pedido_seleccionado.getPrecioTotal()+iva)+"");
        }

    }//GEN-LAST:event_listadoDeOrdenesMouseClicked

