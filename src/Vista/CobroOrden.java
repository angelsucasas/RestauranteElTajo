    public void cargarOrdenes(){
        pedidos = micontrol.getPedidos();
                
        codigos = new ArrayList<>();
        
        DefaultTableModel modelT = (DefaultTableModel) listadoDeOrdenes.getModel();
        modelT.setRowCount(0);
        for(Pedido pedido:pedidos){
            codigos.add(pedido.getCodigo());
            modelT.addRow(new Object[]{pedido.getCodigo(), pedido.getMesa(), pedido.getMesero().conseguirNombreCompleto()/*, pedido.getFechaPedido()*/, pedido.getPrecioTotal(), pedido.getOrdenCobrada()});
        }
        
        ComboBoxMetodosDePagos.removeAllItems();
        ComboBoxMetodosDePagos.setModel(new DefaultComboBoxModel<>(micontrol.getMetodosDePago()));

    }

