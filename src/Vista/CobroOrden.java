    private void pagarOrdenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pagarOrdenActionPerformed
        // TODO add your handling code here:
        if(listadoDeOrdenes.getSelectedRow() != -1 && !NombreClienteAPagar.getText().isBlank()){
            Pedido pedido_seleccionado = (Pedido)micontrol.filtrarPedidoPorCodigo(
                    this.pedidos,
                    codigos.get(listadoDeOrdenes.getSelectedRow())
            );
            if(pedido_seleccionado.getOrdenCobrada()){ JOptionPane.showMessageDialog(rootPane, "el pedido seleccionado ya ha sido pagado");}
            else{
                micontrol.crearVenta(pedido_seleccionado, NombreClienteAPagar.getText(), ComboBoxMetodosDePagos.getSelectedItem().toString(), LocalDateTime.now());
                JOptionPane.showMessageDialog(rootPane, "Venta registrada exitosamente");
                cargarOrdenes();
            }            
        }else{            
            if(listadoDeOrdenes.getSelectedRow() == -1) JOptionPane.showMessageDialog(rootPane, "debe seleccionar la orden a pagar");
            if(NombreClienteAPagar.getText().isBlank()) JOptionPane.showMessageDialog(rootPane, "debe indicar el nombre del cliente a pagar");
        }
    }//GEN-LAST:event_pagarOrdenActionPerformed

