public class Categoria {
    private String nombre;
    private int codigo;
   
    public Categoria(String nombre, int codigo){
        this.codigo=codigo;
        this.nombre=nombre;
    }

    public Categoria(){}
     
    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getCodigo() {
        return codigo;
    }

    public void setCodigo(int codigo) {
        this.codigo = codigo;
    }
    
    
}
